function addCart(id){
	var qty = document.getElementById("qty").value;
	  $.ajax({
		type:'post',
		url:'addtocart.php',
		data:{
		  addcart:'addcart',
		  id:id,
		  qty:qty
		},
		success:function(response) {
		  toastr.success(response+" - Added to the Cart");
		  count();
		}
	  });
  
}

function count(){
	$.ajax({
		type:'post',
		url:'addtocart.php',
		data:{
		  count:'count'
		},
		success:function(response) {
		  document.getElementById("cartCount").innerHTML = response;
		}
	  });
  
}





